function htmlEnc(s) {
  return s.replace(/&/g, '&amp;')
  .replace(/</g, '&lt;')
  .replace(/>/g, '&gt;')
  .replace(/'/g, '&#39;')
  .replace(/"/g, '&#34;');
}

function onlyLettersAndNumbers(str) {
  return /^[A-Za-z0-9_]*$/.test(str);
}
    
